package mainPackage;

public class Class {
		int ID; 
		String ClassName;
		
		
		public Class(int iD, String className) {
			super();
			ID = iD;
			ClassName = className;
		}


		@Override
		public String toString() {
			return "Class [ID=" + ID + ", ClassName=" + ClassName + "]";
		} 
		
		
		
		
}	
